"""
core/slam_validator.py — ORBSLAM3 / evo trajectory validation.

Runs ORBSLAM3 as a subprocess on EuRoC-format extracted sessions,
parses TUM-format trajectory files, computes drift metrics, and
aligns SLAM trajectories with GPS ground truth via Umeyama alignment.

No UI imports — pure Python business logic.
"""
from __future__ import annotations

import shutil
import subprocess
from pathlib import Path
from typing import Callable

import numpy as np
from loguru import logger

from core.models import (
    CalibrationResult,
    GPSSample,
    SLAMResult,
    TrajectoryMetrics,
    TrajectoryPoint,
)

ProgressCB = Callable[[int], None]
StatusCB = Callable[[str], None]

