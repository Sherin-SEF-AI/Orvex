# ---------------------------------------------------------------------------
# Installation checks
# ---------------------------------------------------------------------------

def check_slam_installation() -> dict:
    """Check availability of ORBSLAM3, VINS-Mono (ROS), and evo.

    Returns:
        dict with keys: orbslam3, vins_mono, evo — each with sub-fields.
    """
    orbslam3_path = shutil.which("ORBSLAM3") or shutil.which("orbslam3")

    try:
        import evo  # noqa: F401
        evo_installed = True
    except ImportError:
        evo_installed = False

    ros_available = bool(shutil.which("roscore"))

    return {
        "orbslam3": {
            "installed": orbslam3_path is not None,
            "path": orbslam3_path,
        },
        "vins_mono": {
            "installed": False,  # Requires ROS and vins_mono_node
            "ros_available": ros_available,
        },
        "evo": {
            "installed": evo_installed,
        },
    }


